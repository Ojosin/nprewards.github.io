  data(){
    return{
      dis : false,
      text_login:'Login',
      data : {
        username:'',
        password:''
      }
    }
  },
  created(){
    if(this.isAuth){
      document.location.href = "/dashboard";
    }
  },
  computed:{
    ...mapGetters(['isAuth']),
    ...mapState(['errors'])
  },
  methods:{
    ...mapActions('auth',['submit']),
    ...mapMutations(['CLEAR_ERRORS']),
    postLogin(e){
      e.preventDefault();

      this.submit(this.data).then(()=>{
        this.dis = true;
        this.text_login = 'Loading ....';
        if(this.isAuth){
          this.CLEAR_ERRORS();
           this.$router.push({name:'dashboard'})
        }
        else
        {
          alert('gagal');
        }
      })
    }
  }
}
